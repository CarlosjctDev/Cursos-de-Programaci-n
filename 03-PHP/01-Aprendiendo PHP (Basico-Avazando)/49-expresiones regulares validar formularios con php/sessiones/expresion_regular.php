    -En el área de la programacion, las expresiones regulares son un método
    por medio del cual se puede realizar búsquedas dentro de una cadenas de
    caracteres. Sin importar la amplitud de la búsquedas requerida de un patron
    definido de caracteres, las expresiones regulares proporcionan una solución
    práctica al problema.
