<!-- Estructura repetitivas (ciclos|bucles|loops)
    Son aquellas que reiteran una o un grupo de instruciones "n" veces y
    dependen de una variable de control del ciclo llamada contador.
    Es decir, ejecutan una o varias instrucciones un número de veces definido.

    Los cuatro bucles en PHP son el bucle while, el bucle for, el bucle do-while
    y el bucle foreach.

    Ciclo while
    while(mientras) ejecuta una porción de programa mientras se cumpla una cierta condición.
    Mientras la condición sea verdadera, se ejecutan las instrucciones contenidas en el while.
    Cuando deja de cumplirse la condición, se sale del ciclo y se continua ejecutando el resto
    del programa.

    while(condicion){codigo a ejecutar}
    while(condicion): Codigo a ejecutar endwhile;

    
