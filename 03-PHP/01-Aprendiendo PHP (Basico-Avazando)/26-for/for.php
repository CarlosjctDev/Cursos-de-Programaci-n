<!-- Ciclo for
    For (para) se utiliza generalmente cuando tenemos bien determinada la cantidad
    de repeticiones a realizar. Se diferencia de las anteriores en que se
    debe incluir en la propia instrucción una variable de control, la cual
    se incrementa o decrementa de forma automática.

    dos sintaxis
    for(var;condicion;incre|decre){codigo a ejecutar}
    for(var;condicion;incre|decre): codigo a ejecutar endfor;

-->